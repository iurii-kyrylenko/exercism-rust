use variable_length_quantity::*;

fn main() {
    let res = from_bytes(&[
        // 0x00,
        // 0xc0, 0x00,
        // 0xc8, 0xe8, 0x56,
        // 0xff, 0xff, 0xff, 0x7f,
        // 0xff, 0xff, 0xff, 0xff, 0x0f,

        // overflow
        0xff, 0xff, 0xff, 0xff, 0x7f,
        0x90, 0xff, 0xff, 0xff, 0x7f,

        // max_number = 0xffff_ffff
        0x8f, 0xff, 0xff, 0xff, 0x7f,

        // ok
        0x8e, 0xff, 0xff, 0xff, 0x7f,
        0x8d, 0xff, 0xff, 0xff, 0x7f,

        // 0x00,
        // 0xff, 0x7f,
        // 0x81, 0x80, 0x00,
    ]);

    println!("{:?}", res);

    println!("===============");

    println!("{:?}", number_to_bytes(&0xffff_ffff));
}
